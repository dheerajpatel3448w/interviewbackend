// ─────────────────────────────────────────────────────────────
// PUT /api/roadmap/:id/topic/:topicId/complete
// Mark a topic as completed and recalculate completion %
// ─────────────────────────────────────────────────────────────
export const markTopicComplete = asyncHandler(async (req, res) => {
  const roadmap = await Roadmap.findOne({ _id: req.params.id, user: req.user.id });
  if (!roadmap) return errorResponse(res, "Roadmap not found", 404);

  let totalTopics = 0;
  let completedTopics = 0;

  roadmap.weeks.forEach((week) => {
    week.topics.forEach((topic) => {
      if (topic._id.toString() === req.params.topicId) {
        topic.completed = true;
      }
      totalTopics++;
      if (topic.completed) completedTopics++;
    });
  });

  roadmap.completionPercent = totalTopics > 0 ? Math.round((completedTopics / totalTopics) * 100) : 0;
  await roadmap.save();

  return successResponse(res, { completionPercent: roadmap.completionPercent }, "Topic marked complete");
});

// ─────────────────────────────────────────────────────────────
// DELETE /api/roadmap/:id
// Archive (soft delete) a roadmap
// ─────────────────────────────────────────────────────────────
export const deleteRoadmap = asyncHandler(async (req, res) => {
  const roadmap = await Roadmap.findOneAndUpdate(
    { _id: req.params.id, user: req.user.id },
    { isActive: false }
  );
  if (!roadmap) return errorResponse(res, "Roadmap not found", 404);
  return successResponse(res, null, "Roadmap removed");
});
