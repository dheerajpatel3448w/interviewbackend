// ─────────────────────────────────────────────────────────────
// Helper: Aggregate recurring weak areas from multiple sessions
// ─────────────────────────────────────────────────────────────
const aggregateWeaknesses = (interviews) => {
  const frequencyMap = {};
  for (const interview of interviews) {
    const combined = [...(interview.mistakes || []), ...(interview.improvements || [])];
    for (const item of combined) {
      const key = item.toLowerCase().substring(0, 80);
      frequencyMap[key] = (frequencyMap[key] || 0) + 1;
    }
  }
  // Return items that appear in more than one session (truly recurring)
  return Object.entries(frequencyMap)
    .filter(([, count]) => count > 1)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 8)
    .map(([text]) => text);
};

// ─────────────────────────────────────────────────────────────
// Helper: Build session history summary for AI prompt
// ─────────────────────────────────────────────────────────────
const buildSessionHistory = (interviews) => {
  return interviews.map((iv, i) => ({
    session: interviews.length - i,
    overall: iv.overall,
    date: iv.createdAt,
  }));
};

// ─────────────────────────────────────────────────────────────
// POST /api/roadmap/generate
// AI generates personalized roadmap using last 3 sessions
// ─────────────────────────────────────────────────────────────
export const generateRoadmap = asyncHandler(async (req, res) => {
  const userId = req.user.id;
  const { interviewId } = req.body;

  let targetInterview;
  if (interviewId) {
    targetInterview = await InterviewEvaluation.findOne({ _id: interviewId, user: userId });
    if (!targetInterview) return errorResponse(res, "Interview not found.", 404);
  } else {
    targetInterview = await InterviewEvaluation.findOne({ user: userId }).sort({ createdAt: -1 });
  }

  if (!targetInterview) {
    return errorResponse(res, "No interview found. Complete an interview first.", 404);
  }

  // Fetch last 3 interviews for the same role/level to find recurring weaknesses
  const recentSessions = await InterviewEvaluation.find({
    user: userId,
    role: targetInterview.role,
    level: targetInterview.level,
  })
    .sort({ createdAt: -1 })
    .limit(3)
    .select("mistakes improvements overall createdAt");

  const recurringWeaknesses = aggregateWeaknesses(recentSessions);
  const sessionHistory = buildSessionHistory(recentSessions);

  // Determine trend for AI context
  const scores = recentSessions.map((s) => s.overall);
  const trendLabel =
    scores.length >= 2
      ? scores[0] > scores[scores.length - 1]
        ? "improving"
        : scores[0] < scores[scores.length - 1]
        ? "declining"
        : "plateauing"
      : "single session";

  const roadmapData = await generateStudyRoadmap({
    role: targetInterview.role,
    level: targetInterview.level,
    techstack: targetInterview.techstack,
    mistakes: targetInterview.mistakes,
    improvements: targetInterview.improvements,
    overall: targetInterview.overall,
    recurringWeaknesses,
    sessionHistory,
    trend: trendLabel,
  });

  const roadmap = await Roadmap.create({
    user: userId,
    interviewId: targetInterview._id,
    title: roadmapData.title,
    summary: roadmapData.summary,
    estimatedHours: roadmapData.estimatedHours,
    weeks: roadmapData.weeks,
    keySkillsToMaster: roadmapData.keySkillsToMaster,
    practiceProjects: roadmapData.practiceProjects,
    role: targetInterview.role,
    level: targetInterview.level,
    techstack: targetInterview.techstack,
    overallScore: targetInterview.overall,
    basedOnSessions: recentSessions.length,
    autoGenerated: false,
  });

  // Link roadmap back to interview
  await InterviewEvaluation.findByIdAndUpdate(targetInterview._id, { roadmapId: roadmap._id });

  return successResponse(res, roadmap, "Roadmap generated successfully", 201);
});

